# Funcion para saber el tipo de datos que algo es = type()
print(type('Hello'))
print(type(123))
print(type(0.1526))
print(type(True))



# STRINGS = texto o cadenas de texto
# Subscripting
# Para sacar el caracter del string se pone la posicion entre corchetes
# Si el numero entre corchetes es negativo arranca a buscar de derecha a izquierda
print('Hello'[-1])

# Integer = Whole number
print( 123+ 345 )
# La funcion LEN devuelve un INT
print(type(len('123')))

# Large Integers
print(123_456_789)

# Float = Floating Point / Numeros con Decimales
print(3.141516)

# Boolean, Se escriben con la primer letra mayuscula
print(True)
print(False)